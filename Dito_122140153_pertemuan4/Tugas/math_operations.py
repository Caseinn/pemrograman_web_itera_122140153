PI = 3.14159  # konstanta

def luas_persegi(s):
    return s * s

def keliling_persegi(s):
    return 4 * s

def luas_persegi_panjang(p, l):
    return p * l

def keliling_persegi_panjang(p, l):
    return 2 * (p + l)

def luas_lingkaran(r):
    return PI * r * r

def keliling_lingkaran(r):
    return 2 * PI * r

def c_to_f(c):
    return (c * 9/5) + 32

def c_to_k(c):
    return c + 273.15
